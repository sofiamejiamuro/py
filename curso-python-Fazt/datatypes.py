# Tipos de datos

# STRING - Cadena de caracteres

print('STRING')

print(type('Hello World'))

# Concat

print("Hello World" + " from Python")

# NUMBERS

# integer
print(type(30)) #int 

# float - decimal
print(type(30.5)) #float

# BOOLEAN True/False

print(type(True)) #bool

# LIST: numeros, strings, se pueden combinar tipos de datos, cada uno es un elemento

print([10, 20, 30, 40, 50])
print(type([10, 20, 30, 40, 50])) #list
[]

# TUPLES: una lista inmutable
print((10, 20, 30, 40, 50))
print(type((10, 20, 30, 40, 50))) #tuple
()

# DICTIONARIES: agrupa datos que pertenecen a una misma entidad, par clave - valor, un objeto 
{
  "name":"Sofia",
  "lastName": "Mejia",
  "age": 31
}

print(type({
  "name":"Sofia",
  "lastName": "Mejia",
  "age": 31
})) #dict

# NONE 
print(type(None))



